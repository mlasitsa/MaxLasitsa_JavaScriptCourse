/*
    UNIT 15: OPERATORS 
    Types of Operators:
    
    Arithmetic Operators: Used for performing mathematical calculations.
    + (Addition): Adds two operands(For example a + b, where a and b are operands).
    - (Subtraction): Subtracts the second operand from the first.
    * (Multiplication): Multiplies two operands.
    / (Division): Divides the first operand by the second.
    % (Modulus): Returns the remainder of dividing two operands.
    ++ (Increment): Increases an integer value by one.
    -- (Decrement): Decreases an integer value by one.

    Comparison Operators: Used to compare two values.
    == (Equal to): Checks if two values are equal.
    === (Strictly equal to): Checks if two values are equal in value and type.
    != (Not equal to): Checks if two values are not equal.
    !== (Strictly not equal to): Checks if two values are not equal in either value or type.
    >, <, >=, <=: Greater than, less than, greater than or equal to, less than or equal to.

    Logical Operators: Used to determine the logic between variables or values.
    && (Logical AND): Returns true if both operands are true.
    || (Logical OR): Returns true if at least one of the operands is true.
    ! (Logical NOT): Returns true if the operand is false and vice versa.

    Assignment Operators: Used to assign values to variables.
    = (Assignment): Assigns the right operand's value to the left operand.
    +=, -=, *=, /=, %=: Perform the operation and assign the result.

    Type Operators:
    typeof: Returns a string indicating the type of the operand.
    instanceof: Returns true if the specified object is an instance of a specified object type.
    new: Is used to create an instance of an object type. It instantiates a new object from a constructor function or a class.
    delete: Is used to remove a property from an object.
*/
